import Foundation

/// This enum defines the available block style in the game.
/// It also defines their resource identifiers.
/// You can use this property to style the .tesla and .styled blocks.
enum BlockStyle: String {
    case red = "Red"
    case blue = "Blue"
    case green = "Green"
    case yellow = "Yellow"
}
